while True:
    try:
        c = float(input("一つ目の数字を入力してください。"))
        operator = input("演算子を入力してください。(+,-,*,/)")
        b = float(input("二つ目の数字を入力してください。"))
        if operator == "+":
            answertext = float(c + b)
        elif operator == "-":
            answertext = float(c - b)
        elif operator == "*":
            answertext = float(c * b)
        elif operator == "/":
            answertext = float(c / b)
        a.write(c,font = (0,43))
        time.sleep(1)
        a.clear()
        a.write(operator,font = (0,43))
        time.sleep(1)
        a.clear()
        a.write(b,font = (0,43))
        time.sleep(1)
        a.clear()
        a.write("=",font = (0,43))
        time.sleep(1)
        a.clear()
        a.write(answertext,font = (0,43))
    except ValueError:
        print("数字を入力してください。")
    except NameError:
        print("+,-,*,/,のどれかを入力してください。")
    except ZeroDivisionError:
        print("/の時は、二つ目に0を入れないでください。")
    else:
        t.fd(100000)
        end = input("終了しますか？（yes,no）")
        if end == "yes":
            break
        else:
            a.clear()
